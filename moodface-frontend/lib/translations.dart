  static String t(String key) {
    final lang = DataStore().appLanguage;
    
    // 1. Check main translations
    final map = _localizedValues[lang];
    if (map != null && map.containsKey(key)) {
      return map[key]!;
    }
    
    // 2. Check notification translations
    final notifMap = _notificationValues[lang];
    if (notifMap != null && notifMap.containsKey(key)) {
      return notifMap[key]!;
    }

    // 3. Check dashboard translations
    final dashMap = _dashboardValues[lang];
    if (dashMap != null && dashMap.containsKey(key)) {
      return dashMap[key]!;
    }

    // Fallbacks to French
    final frenchMap = _localizedValues['Français']!;
    if (frenchMap.containsKey(key)) {
      return frenchMap[key]!;
    }
    final frenchNotifMap = _notificationValues['Français']!;
    if (frenchNotifMap.containsKey(key)) {
      return frenchNotifMap[key]!;
    }
    final frenchDashMap = _dashboardValues['Français']!;
    if (frenchDashMap.containsKey(key)) {
      return frenchDashMap[key]!;
    }

    // Fallbacks to English
    final englishMap = _localizedValues['English'];
    if (englishMap != null && englishMap.containsKey(key)) {
      return englishMap[key]!;
    }
    final englishNotifMap = _notificationValues['English'];
    if (englishNotifMap != null && englishNotifMap.containsKey(key)) {
      return englishNotifMap[key]!;
    }
    final englishDashMap = _dashboardValues['English'];
    if (englishDashMap != null && englishDashMap.containsKey(key)) {
      return englishDashMap[key]!;
    }

    return key;
  }

  static List<String> get supportedLanguages => _localizedValues.keys.toList();

  static String translateFrequency(String frequency) {
    if (frequency == "Jour") return t('period_day');
    if (frequency == "Semaine") return t('period_week');
    if (frequency == "Mois") return t('period_month');
    return frequency;
  }

  static String translateEmotion(String emotion) {
    final key = emotion.toLowerCase();
    if (key == 'heureux' || key == 'happy') return t('emotion_happy');
    if (key == 'triste' || key == 'sad') return t('emotion_sad');
    if (key == 'neutre' || key == 'neutral') return t('emotion_neutral');
    if (key == 'en colère' || key == 'colère' || key == 'angry') return t('emotion_angry');
    if (key == 'surpris' || key == 'surprise') return t('emotion_surprise');
    if (key == 'peur' || key == 'fear') return t('emotion_fear');
    if (key == 'dégoût' || key == 'degout' || key == 'disgust') return t('emotion_disgust');
    return emotion;
  }
