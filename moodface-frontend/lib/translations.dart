  static String translateEmotion(String emotion) {
    final key = emotion.toLowerCase();
    if (key == 'heureux' || key == 'happy') return t('emotion_happy');
    if (key == 'triste' || key == 'sad') return t('emotion_sad');
    if (key == 'neutre' || key == 'neutral') return t('emotion_neutral');
    if (key == 'en colère' || key == 'colère' || key == 'angry') return t('emotion_angry');
    if (key == 'surpris' || key == 'surprise') return t('emotion_surprise');
    if (key == 'peur' || key == 'fear') return t('emotion_fear');
    if (key == 'dégoût' || key == 'degout' || key == 'disgust') return t('emotion_disgust');
    return emotion;
  }
